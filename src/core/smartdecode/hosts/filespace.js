/**
 * SmartDecode 2.0 - Filespace.com Extractor
 */

const FilespaceExtractor = {
    PATTERNS: [
        /https?:\/\/(?:www\.)?filespace\.com\/([a-zA-Z0-9]+)/i
    ],

    extract(input) {
        if (!input || typeof input !== 'string') return [];
        const candidates = new Map();
        this.PATTERNS.forEach(regex => {
            let match;
            const localRegex = new RegExp(regex, 'gi');
            while ((match = localRegex.exec(input)) !== null) {
                const url = match[0];
                const fileId = match[1];
                if (!candidates.has(url)) {
                    candidates.set(url, {
                        url, fileId, filename: 'unknown',
                        host: 'filespace.com',
                        type: 'file',
                        sourceLayer: 'host_filespace',
                        confidence: 0.95
                    });
                }
            }
        });
        return Array.from(candidates.values());
    }
};

if (typeof module !== 'undefined' && module.exports) {
    module.exports = FilespaceExtractor;
}
